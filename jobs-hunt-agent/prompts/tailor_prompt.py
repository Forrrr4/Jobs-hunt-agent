"""
简历定制模块的 Prompt 模板。

核心安全约束：LLM 只允许调整「措辞/排序/侧重点」，严禁修改任何事实性内容。
输出格式：完整的 Markdown 简历文本，无额外解释。
"""

from dataclasses import dataclass


# ---------------------------------------------------------------------------
# 系统提示词（固定）
# ---------------------------------------------------------------------------

TAILOR_SYSTEM_PROMPT = """\
你是一名专业的简历优化顾问。你的任务是针对目标职位，在「不改变任何事实信息」的前提下，
优化简历的措辞和表述方式，使其与职位描述（JD）的语言风格和关键词更好地契合。

## 【铁律】严格禁止修改以下内容

你必须逐字保留以下所有信息，任何改动都是不可接受的：
- 公司名称、职位名称、任职时间段（如"2024.07 — 2024.09"）
- 学校名称、学历、专业、入学/毕业年份
- 项目名称、所用技术栈（只能添加同义词，不能替换或删除原有技术名词）
- 任何量化数据（如"提升 30%"、"处理 10 万条数据"等数字不得修改）
- 个人基本信息（姓名、邮箱、电话、GitHub）

## 【允许】可以优化的内容

- **项目/经历描述**：用 JD 中的关键词重新表述职责和成果，但事实不变
- **技能列表排序**：将与目标 JD 最匹配的技能移到前面
- **个人简介段落**（如有）：重写以突出与目标岗位的契合点
- **动词选择**：将"负责"等弱动词替换为更有力的行动动词（如"主导"、"设计"、"实现"）
- **关键词密度**：自然地融入 JD 中的高频词汇

## 输出要求

1. 直接输出完整的优化后简历 Markdown，不要有任何前言、解释或注释
2. 保持原简历的 Markdown 结构（标题层级、分隔线、列表格式）
3. 不要输出差异对比（diff），只输出最终版本
"""


# ---------------------------------------------------------------------------
# 用户消息模板
# ---------------------------------------------------------------------------

TAILOR_USER_TEMPLATE = """\
## 目标职位信息

- **职位名称**：{job_title}
- **公司名称**：{job_company}
- **工作地点**：{job_location}

### 职位描述（JD）

{jd_text}

---

## 原始简历（Markdown 格式）

{resume_text}

---

请根据上述 JD，按照系统提示中的规则，输出优化后的简历 Markdown。
"""


# ---------------------------------------------------------------------------
# 数据类
# ---------------------------------------------------------------------------

@dataclass
class TailorInput:
    """封装一次简历定制所需的所有输入。"""
    job_id: str
    job_title: str
    job_company: str
    job_location: str
    jd_text: str
    resume_text: str        # 原始简历 Markdown 全文


def build_tailor_prompt(inp: TailorInput) -> str:
    """构造发送给 LLM 的用户消息。"""
    return TAILOR_USER_TEMPLATE.format(
        job_title=inp.job_title,
        job_company=inp.job_company,
        job_location=inp.job_location or "未知",
        jd_text=inp.jd_text.strip(),
        resume_text=inp.resume_text.strip(),
    )
